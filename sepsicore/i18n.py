def normalize_language(language: str | None) -> str:
    return language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def t(language: str, key: str) -> str:
    lang = normalize_language(language)
    return TEXT[lang].get(key, TEXT[DEFAULT_LANGUAGE].get(key, key))


def analysis_t(language: str, key: str) -> str:
    lang = normalize_language(language)
    return ANALYSIS_TEXT[lang].get(key, ANALYSIS_TEXT[DEFAULT_LANGUAGE].get(key, key))


def validation_t(language: str, key: str, **kwargs) -> str:
    lang = normalize_language(language)
    template = VALIDATION_TEXT[lang].get(key, VALIDATION_TEXT[DEFAULT_LANGUAGE].get(key, key))
    return template.format(**kwargs)


def report_t(language: str, key: str, **kwargs) -> str:
    lang = normalize_language(language)
    template = REPORT_TEXT[lang].get(key, REPORT_TEXT[DEFAULT_LANGUAGE].get(key, key))
    return template.format(**kwargs)
